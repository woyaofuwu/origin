UPDATE TD_M_RES_STOCKJOB T
   SET T.WARNNING_VALUE_U = T.WARNNING_VALUE_U + :WARNNING_VALUE_U
 WHERE T.STOCK_POS = :STOCK_POS
   AND T.RES_TYPE_CODE = 'A'
   AND T.RSRV_TAG1 = 'A'
   AND T.CITY_CODE = :CITY_CODE
   AND EPARCHY_CODE = :EPARCHY_CODE
   AND T.WARNNING_VALUE_U >= 0
   AND T.WARNNING_VALUE_U <= T.WARNNING_VALUE_D - :WARNNING_VALUE_U