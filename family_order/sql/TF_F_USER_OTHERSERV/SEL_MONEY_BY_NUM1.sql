SELECT PARTITION_ID,USER_ID,SERVICE_MODE,SERIAL_NUMBER,PROCESS_INFO,RSRV_NUM1,RSRV_NUM2,RSRV_NUM3,
       RSRV_STR1,RSRV_STR2,RSRV_STR3,RSRV_STR4,RSRV_STR5,RSRV_STR6,RSRV_STR7,RSRV_STR8,RSRV_STR9,RSRV_STR10,
       RSRV_DATE1,RSRV_DATE2,RSRV_DATE3,PROCESS_TAG,STAFF_ID,DEPART_ID,START_DATE,END_DATE,REMARK
  FROM TF_F_USER_OTHERSERV 
 WHERE partition_id = MOD(to_number(:USER_ID),10000)
   AND user_id=TO_NUMBER(:USER_ID)
   AND rsrv_num1 = :RSRV_NUM1
   AND process_tag = :PROCESS_TAG
   AND service_mode=:SERVICE_MODE
   AND end_date > sysdate