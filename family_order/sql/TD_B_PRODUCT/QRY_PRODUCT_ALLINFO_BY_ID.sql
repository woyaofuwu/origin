SELECT A.PRODUCT_ID,
       A.PRODUCT_NAME,
       A.PRODUCT_EXPLAIN,
       A.PRODUCT_MODE,
       A.NET_TYPE_CODE,
       A.BRAND_CODE,
       A.GROUP_BRAND_CODE,
       A.PRODUCT_OBJ_TYPE,
       A.RES_TYPE_CODE,
       A.COMP_TAG,
       A.ENABLE_TAG,
       A.START_ABSOLUTE_DATE,
       A.START_OFFSET,
       A.START_UNIT,
       A.END_ENABLE_TAG,
       A.END_ABSOLUTE_DATE,
       A.END_OFFSET,
       A.END_UNIT,
       A.CANCEL_TAG,
       A.CANCEL_ABSOLUTE_DATE,
       A.CANCEL_OFFSET,
       A.CANCEL_UNIT,
       TO_CHAR(A.START_DATE, 'YYYY-MM-DD HH24:MI:SS') AS START_DATE,
       TO_CHAR(A.END_DATE, 'YYYY-MM-DD HH24:MI:SS') AS END_DATE,
       A.MIN_NUMBER,
       A.MAX_NUMBER,
       A.CREATE_DATE,
       A.VERSION,
       A.PRODUCT_STATE,
       A.RELEASE_TAG,
       A.PRIORITY,
       A.PREPAY_TAG,
       A.TAG_SET,
       A.CAMPN_ID,
       A.EXPERIENCE_MONTHS,
       A.EXPERIENCE_LIMIT,
       A.EXPERIENCE_LIMIT_DAYS,
       A.UPDATE_TIME,
       A.UPDATE_STAFF_ID,
       A.UPDATE_DEPART_ID,
       A.REMARK,
       A.RSRV_STR1,
       A.RSRV_STR2,
       A.RSRV_STR3,
       A.RSRV_STR4,
       A.RSRV_STR5,
       A.RSRV_DATE1,
       A.RSRV_DATE2,
       A.RSRV_DATE3,
       A.RSRV_TAG1,
       A.RSRV_TAG2,
       A.RSRV_TAG3,
       A.ALIAS_ID,
       A.MAIN_TAG,
       A.PRICE
  FROM TD_B_PRODUCT A
 WHERE A.PRODUCT_ID = :PRODUCT_ID
   AND SYSDATE BETWEEN A.START_DATE AND A.END_DATE