SELECT SERIAL_NUMBER,
        USER_ID,
        PRODUCT_ID,
        PACKAGE_ID,
        SMS_CODE,
        CHECK_COUNT,
        EPARCHY_CODE,
        UPDATE_TIME,
        INDATE,
        RSRV_STR1,
        RSRV_STR2,
        RSRV_STR3,
        RSRV_STR4,
        RSRV_STR5
   FROM TF_F_USER_VALIDCODE t
  WHERE SERIAL_NUMBER = :SERIAL_NUMBER
    AND USER_ID = :USER_ID
    AND PRODUCT_ID = :PRODUCT_ID
    AND EPARCHY_CODE = :EPARCHY_CODE
    AND TO_CHAR(INDATE, 'YYYY-MM-DD')= TO_CHAR(sysdate, 'YYYY-MM-DD')