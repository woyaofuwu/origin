select T.ACCT_ID
  from TF_A_PAYRELATION T, TF_F_RELATION_AA A
 where T.USER_ID = TO_NUMBER(:USER_ID)
   and T.PARTITION_ID = mod(TO_NUMBER(:USER_ID), 10000)
   AND T.ACT_TAG = '1'
   AND TO_CHAR(SYSDATE, 'YYYYMMDD') BETWEEN START_CYCLE_ID AND END_CYCLE_ID
   AND (A.ACCT_ID_A = T.ACCT_ID OR A.ACCT_ID_B = T.ACCT_ID)
   AND A.PARTITION_ID = MOD(A.ACCT_ID_B, 10000)
   AND A.END_DATE >= to_char(SYSDATE,'YYYYMMDD')