SELECT A.CITY_CODE CITY_CODE, A.SERIAL_NUMBER SERIAL_NUMBER,
       B.CUST_NAME CUST_NAME_A, A.CUST_NAME CUST_NAME_B,
       TO_CHAR(A.FINISH_DATE, 'yyyy-mm-dd hh24:mi:ss') FINISH_DATE
  FROM TF_B_TRADE_CUSTOMER_BAK B, TF_BH_TRADE A
 WHERE B.TRADE_ID = A.TRADE_ID
   AND B.ACCEPT_MONTH = TO_NUMBER(SUBSTR(A.TRADE_ID, 5, 2))
   AND A.ACCEPT_DATE + 0 >= TO_DATE(:START_DATE, 'yyyy-mm-dd')
   AND A.ACCEPT_DATE + 0 < TO_DATE(:END_DATE, 'yyyy-mm-dd') + 1
   AND A.CANCEL_TAG = '0'
   AND A.CITY_CODE = :AREA_CODE
   AND A.TRADE_TYPE_CODE = 100
