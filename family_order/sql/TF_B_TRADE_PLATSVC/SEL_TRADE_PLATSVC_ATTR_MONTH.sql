SELECT TO_CHAR(A.SUBSCRIBE_ID) SUBSCRIBE_ID,
       TO_CHAR(A.TRADE_ID) TRADE_ID,
       A.ACCEPT_MONTH,
       TO_CHAR(A.USER_ID) USER_ID,
       A.SERVICE_ID,
       A.SERIAL_NUMBER,
       A.SP_CODE,
       A.BIZ_CODE,
       A.BIZ_TYPE_CODE,
       A.BIZ_STATE_CODE,
       A.PRODUCT_NO,
       A.ORG_DOMAIN,
       A.OPER_CODE,
       A.OPR_SOURCE,
       A.BILL_TYPE,
       A.PRICE,
       A.PKGSEQ,
       A.UDSUM,
       A.INTF_TRADE_ID,
       B.INFO_CODE,
       B.INFO_VALUE,
       B.RSRV_STR1,
       B.RSRV_STR2
  FROM TF_B_TRADE_PLATSVC A,TF_B_TRADE_PLATSVC_ATTR B
 WHERE 1=1
   AND A.TRADE_ID = B.TRADE_ID   
   AND B.INFO_CODE = :INFO_CODE
   AND A.USER_ID = TO_NUMBER(:USER_ID)
   AND A.SERVICE_ID = TO_NUMBER(:SERVICE_ID)
   AND A.BIZ_TYPE_CODE = TO_NUMBER(:BIZ_TYPE_CODE)
   AND A.SP_CODE = TO_NUMBER(:SP_CODE)
   AND A.BIZ_CODE = TO_NUMBER(:BIZ_CODE)
   AND A.OPER_CODE = TO_NUMBER(:OPER_CODE)
   AND TRUNC(A.UPDATE_TIME,'MM') = TRUNC(SYSDATE,'MM')
