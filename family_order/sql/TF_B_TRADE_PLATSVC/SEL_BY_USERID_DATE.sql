SELECT SERIAL_NUMBER,PRICE,TO_CHAR(START_DATE,'YYYY-MM-DD HH24:MI:SS'),
       BIZ_TYPE_CODE,ORG_DOMAIN,SERVICE_ID,SP_CODE,BIZ_CODE
FROM TF_B_TRADE_PLATSVC
WHERE 1=1
AND USER_ID = :USER_ID
AND START_DATE >= TO_DATE(:START_TIME,'YYYY-MM-DD HH24:MI:SS')
AND START_DATE <= TO_DATE(:END_TIME,'YYYY-MM-DD HH24:MI:SS')
AND ORG_DOMAIN = 'CMGP'
AND OPER_CODE = '16'