SELECT TRADE_TYPE_CODE, BRAND_CODE, PRODUCT_ID, TRADE_ATTR, RECEIPT_INFO1,
       RECEIPT_INFO2, RECEIPT_INFO3, RECEIPT_INFO4, RECEIPT_INFO5,
       START_DATE, END_DATE, EPARCHY_CODE, REMARK
  FROM TD_B_TRADE_RECEIPT
 WHERE (TRADE_TYPE_CODE = :TRADE_TYPE_CODE)
   AND (BRAND_CODE = :BRAND_CODE)
   AND (PRODUCT_ID = :PRODUCT_ID)
   AND (TRADE_ATTR = :TRADE_ATTR)
   AND (EPARCHY_CODE = :EPARCHY_CODE)
   AND START_DATE = TO_DATE(:START_DATE, 'YYYY-MM-DD HH24:MI:SS')