   AND A.USER_ID = :USER_ID
   --AND A.SERVICE_ID = B.SERVICE_ID
   AND A.END_DATE > SYSDATE
   /*AND B.SP_CODE = C.SP_CODE
   AND B.BIZ_CODE = C.BIZ_CODE
   AND C.SP_CODE = D.SP_CODE
   AND B.ORG_DOMAIN = E.ORG_DOMAIN
