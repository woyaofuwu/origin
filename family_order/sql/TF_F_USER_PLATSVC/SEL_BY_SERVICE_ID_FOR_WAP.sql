SELECT A.SERVICE_ID,        
       TO_CHAR(A.START_DATE, 'YYYYMMDDHH24MISS') START_DATE,
       TO_CHAR(A.END_DATE, 'YYYYMMDDHH24MISS') END_DATE,
       B.PARA_CODE1,
       B.PARA_CODE2
  FROM TF_F_USER_PLATSVC A, TD_S_COMMPARA B
 WHERE A.SERVICE_ID = B.PARA_CODE1
   --AND A.SERVICE_ID = C.SERVICE_ID
   AND A.USER_ID = :USER_ID
   AND B.SUBSYS_CODE = :SUBSYS_CODE
   AND B.PARAM_ATTR = :PARAM_ATTR
   AND B.PARAM_CODE = :PARAM_CODE
   AND A.PARTITION_ID = MOD(:USER_ID, 10000)
   AND A.BIZ_STATE_CODE = 'A'
   AND SYSDATE BETWEEN A.START_DATE AND A.END_DATE