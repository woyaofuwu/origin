SELECT A.PARTITION_ID,
       A.USER_ID,
       A.SERVICE_ID,
       A.BIZ_STATE_CODE,
       TO_CHAR(A.FIRST_DATE, 'yyyy-mm-dd hh:mi:ss') AS FIRST_DATE,
       TO_CHAR(A.FIRST_DATE_MON, 'yyyy-mm-dd hh:mi:ss') AS FIRST_DATE_MON,
       A.GIFT_SERIAL_NUMBER,
       A.GIFT_USER_ID,
       A.START_DATE,
       A.END_DATE,
       TO_CHAR(A.UPDATE_TIME, 'yyyy-mm-dd hh:mi:ss') AS UPDATE_TIME,
       A.UPDATE_STAFF_ID,
       A.UPDATE_DEPART_ID,
       A.REMARK,
       A.RSRV_NUM1,
       A.RSRV_NUM2,
       A.RSRV_NUM3,
       A.RSRV_NUM4,
       A.RSRV_NUM5,
       A.RSRV_STR1,
       A.RSRV_STR2,
       A.RSRV_STR3,
       A.RSRV_STR4,
       A.RSRV_STR5,
       A.RSRV_STR6,
       A.RSRV_STR7,
       A.RSRV_STR8,
       A.RSRV_STR9,
       A.RSRV_STR10,
       A.RSRV_DATE1,
       A.RSRV_DATE2,
       A.RSRV_DATE3,
       A.RSRV_TAG1,
       A.RSRV_TAG2,
       A.RSRV_TAG3,
       A.INST_ID,
       A.IN_CARD_NO,
       A.ENTITY_CARD_NO
  FROM TF_F_USER_PLATSVC A
 WHERE A.USER_ID = TO_NUMBER(:USER_ID)
   AND A.PARTITION_ID = MOD(:USER_ID, 10000)
   AND A.SERVICE_ID = :SERVICE_ID
   AND A.BIZ_STATE_CODE = :BIZ_STATE_CODE
   AND SYSDATE BETWEEN A.START_DATE AND A.END_DATE