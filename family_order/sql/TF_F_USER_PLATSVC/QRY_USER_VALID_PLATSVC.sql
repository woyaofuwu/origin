SELECT PARTITION_ID,
       USER_ID,
       PRODUCT_ID,
       PACKAGE_ID,
       SERVICE_ID,
       BIZ_STATE_CODE,
       TO_CHAR(FIRST_DATE,'YYYY-MM-DD HH24:MI:SS') AS FIRST_DATE,
       TO_CHAR(FIRST_DATE_MON,'YYYY-MM-DD HH24:MI:SS') AS FIRST_DATE_MON,
       GIFT_SERIAL_NUMBER,
       GIFT_USER_ID,
       TO_CHAR(START_DATE,'YYYY-MM-DD HH24:MI:SS') AS START_DATE,
       TO_CHAR(END_DATE,'YYYY-MM-DD HH24:MI:SS') AS END_DATE,
       TO_CHAR(UPDATE_TIME,'YYYY-MM-DD HH24:MI:SS') AS UPDATE_TIME,
       UPDATE_STAFF_ID,
       UPDATE_DEPART_ID,
       REMARK,
       RSRV_NUM1,
       RSRV_NUM2,
       RSRV_NUM3,
       RSRV_NUM4,
       RSRV_NUM5,
       RSRV_STR1,
       RSRV_STR2,
       RSRV_STR3,
       RSRV_STR4,
       RSRV_STR5,
       RSRV_STR6,
       RSRV_STR7,
       RSRV_STR8,
       RSRV_STR9,
       RSRV_STR10,
       TO_CHAR(RSRV_DATE1,'YYYY-MM-DD HH24:MI:SS') AS RSRV_DATE1,
       TO_CHAR(RSRV_DATE2,'YYYY-MM-DD HH24:MI:SS') AS RSRV_DATE2,
       TO_CHAR(RSRV_DATE3,'YYYY-MM-DD HH24:MI:SS') AS RSRV_DATE3,
       RSRV_TAG1,
       RSRV_TAG2,
       RSRV_TAG3,
       INST_ID,
       IN_CARD_NO,
       ENTITY_CARD_NO
  FROM TF_F_USER_PLATSVC A
 WHERE A.USER_ID = TO_NUMBER(:USER_ID)
   AND A.SERVICE_ID = :SERVICE_ID
   AND A.BIZ_STATE_CODE IN ('A', 'N')
   AND SYSDATE<A.END_DATE
   AND A.PARTITION_ID = MOD(TO_NUMBER(:USER_ID), 10000)
