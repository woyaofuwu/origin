SELECT A.USER_ID,
       A.SERVICE_ID,
       A.SERIAL_NUMBER,
       A.SP_CODE,
       A.BIZ_CODE,
       A.BIZ_TYPE_CODE,
       A.BIZ_STATE_CODE,
       A.OPER_CODE,
       A.OPR_SOURCE,
       A.PRICE,
       A.FIRST_DATE,
       A.GIFT_SERIAL_NUMBER
  FROM TF_F_USER_PLATSVC A
 WHERE A.SERIAL_NUMBER = :SERIAL_NUMBER
 AND   A.USER_ID = :USER_ID
 AND   A.BIZ_TYPE_CODE = '37'
 AND   SYSDATE BETWEEN A.START_DATE AND A.END_DATE
