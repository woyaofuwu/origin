INSERT INTO TI_B_TRANS_PHONE
      (SYNC_SEQUENCE, SYNC_DAY, MODIFY_TAG, TRADE_ID, PHONE_CODE_A,
       PHONE_CODE_B, START_DATE, END_DATE, UPDATE_TIME)
      SELECT :SYNC_SEQUENCE, :SYNC_DAY,
             DECODE(B.MODIFY_TAG, '0', '0', '1', '2', '2', '2', '2'), B.TRADE_ID,
             B.PHONE_CODE_A, B.PHONE_CODE_B, B.START_DATE, B.END_DATE,
             B.UPDATE_TIME
        FROM TF_B_TRADE_TRANS_PHONE B
       WHERE B.TRADE_ID = :TRADE_ID
         AND ACCEPT_MONTH = :ACCEPT_MONTH