--IS_CACHE=Y
SELECT A.RLST      X_RESULTOBJ,
       A.RULE_CODE1 RSRV_STR1,
       A.RULE_CODE2 RSRV_STR2,
       A.RULE_CODE3 RSRV_STR3
  FROM TD_B_PLATSVC_PFRULE A
 WHERE A.ORG_DOMAIN = :ORG_DOMAIN
   AND (A.BIZ_TYPE_CODE = :BIZ_TYPE_CODE OR A.BIZ_TYPE_CODE = 'ZZ')
   AND (A.SERVICE_ID = :SERVICE_ID OR A.SERVICE_ID = '90000000')
   AND (A.IN_MODE_CODE = :IN_MODE_CODE OR A.IN_MODE_CODE = 'Q')
   AND (A.OPR_SOURCE = :OPR_SOURCE OR A.OPR_SOURCE = 'x')
   AND (A.OPER_CODE = :OPER_CODE OR A.OPER_CODE = 'XX')
   AND (NVL(A.RSRV_STR1,0) = (SELECT NVL(MAX(B.RSRV_STR1),0) FROM TD_B_PLATSVC_PFRULE B
   										WHERE B.ORG_DOMAIN = :ORG_DOMAIN
										   AND (B.BIZ_TYPE_CODE = :BIZ_TYPE_CODE OR B.BIZ_TYPE_CODE = 'ZZ')
										   AND (B.SERVICE_ID = :SERVICE_ID OR B.SERVICE_ID = '90000000')
										   AND (B.IN_MODE_CODE = :IN_MODE_CODE OR B.IN_MODE_CODE = 'Q')
										   AND (B.OPR_SOURCE = :OPR_SOURCE OR B.OPR_SOURCE = 'x')
										   AND (B.OPER_CODE = :OPER_CODE OR B.OPER_CODE = 'XX')
										   )
   			)