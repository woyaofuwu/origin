        R.RSRV_STR1 FEE,
        R.RSRV_STR2 ORDER_TYPE,
        R.RSRV_STR3 RES_ID,
        R.RSRV_STR4 CAMPN_ID,
        R.RSRV_STR5 SALEACTIVE_TYPE,
        R.PRODUCT_ID,
