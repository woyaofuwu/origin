        C.END_DATE,
        C.PRODUCT_DESC,
        C.PRODUCT_PRICE,
        C.PRODUCT_TYPE,
        C.CHANGE_TYPE,
        TO_CHAR(C.CREATE_TIME, 'YYYY-MM-DD HH24:MI:SS') CREATE_TIME,
        TO_CHAR(C.CHANGE_TIME, 'YYYY-MM-DD HH24:MI:SS') CHANGE_TIME,
        C.PRODUCT_DEMO,
        TO_CHAR(C.UPDATE_TIME, 'YYYY-MM-DD HH24:MI:SS') UPDATE_TIME,
        C.UPDATE_STAFF_ID,
        C.UPDATE_DEPART_ID,
        C.REMARK,
        C.RSRV_STR1
