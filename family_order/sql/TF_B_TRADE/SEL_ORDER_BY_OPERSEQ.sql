SELECT DECODE(T.SUBSCRIBE_STATE,'0','00','1','01','2','01','3','01','4','01','5','01',
'6','01','7','01','8','01','9','01','A','04','B','01','C','01','D','04',
'E','01','F','01','G','01','H','01','I','03','M','04','P','01','L','01') ORDER_STATUS,
T.RSRV_STR2 QUERY_OPER_SEQ FROM (
(SELECT T1.SUBSCRIBE_STATE,T1.RSRV_STR2,T1.ACCEPT_MONTH
FROM TF_B_TRADE T1)
UNION ALL
(SELECT T2.SUBSCRIBE_STATE,T2.RSRV_STR2,T2.ACCEPT_MONTH
FROM TF_BH_TRADE T2)
) T
WHERE T.ACCEPT_MONTH = :ACCEPT_MONTH AND T.RSRV_STR2 = :OPER_SEQ