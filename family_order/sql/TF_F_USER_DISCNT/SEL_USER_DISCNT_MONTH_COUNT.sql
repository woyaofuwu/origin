SELECT COUNT(*) COUNT_NUM
  FROM TF_F_USER_DISCNT T
 WHERE USER_ID = TO_NUMBER(:USER_ID)
   AND PARTITION_ID = MOD(TO_NUMBER(:USER_ID),10000)
   AND TO_CHAR(T.START_DATE, 'YYYYMM') = TO_CHAR(SYSDATE, 'YYYYMM')
   AND T.DISCNT_CODE IN ('20160322', '20160323', '20161322', '20161323','20181322','20181323','20181324','20181325')
