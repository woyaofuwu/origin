SELECT T.SUB_IBSYSID,
       T.IBSYSID,
       T.NODE_ID,
       T.ACCEPT_MONTH,
       T.PRODUCT_ID,
       T.ORDER_ID,
       T.DEAL_STATE,
       T.DEAL_OPTION,
       T.DEAL_SRC,
       T.DEAL_DESC,
       TO_CHAR(T.DEAL_TIME, 'YYYY-MM-DD HH24:MI:SS') DEAL_TIME,
       T.TRANSFER_PARAM,
       T.ROLE_ID,
       T.WORKFORM_TYPE,
       TO_CHAR(T.INSERT_TIME, 'YYYY-MM-DD HH24:MI:SS') INSERT_TIME,
       T.CITY_CODE,
       T.EPARCHY_CODE,
       T.DEPART_ID,
       T.DEPART_NAME,
       T.STAFF_ID,
       T.STAFF_NAME,
       T.STAFF_PHONE,
       T.REMARK,
       T.RSRV_STR1,
       T.RSRV_STR2,
       T.RSRV_STR3,
       T.RSRV_STR4,
       T.RSRV_STR5,
       T.RSRV_STR6,
       T.RSRV_STR7
  FROM TF_B_EOP_NODE T
 WHERE T.IBSYSID = :IBSYSID
   AND T.NODE_ID = :NODE_ID
 ORDER BY T.INSERT_TIME DESC
