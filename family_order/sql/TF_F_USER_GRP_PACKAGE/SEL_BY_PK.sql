select 
PARTITION_ID, to_char(USER_ID) USER_ID, PRODUCT_ID, PACKAGE_ID, ELEMENT_TYPE_CODE, ELEMENT_ID, DEFAULT_TAG, FORCE_TAG, to_char(START_DATE,'yyyy-mm-dd hh24:mi:ss') START_DATE, to_char(END_DATE,'yyyy-mm-dd hh24:mi:ss') END_DATE, EPARCHY_CODE, to_char(UPDATE_TIME,'yyyy-mm-dd hh24:mi:ss') UPDATE_TIME, UPDATE_STAFF_ID, UPDATE_DEPART_ID, REMARK, RSRV_NUM1, RSRV_NUM2, RSRV_NUM3, to_char(RSRV_NUM4) RSRV_NUM4, to_char(RSRV_NUM5) RSRV_NUM5, RSRV_STR1, RSRV_STR2, RSRV_STR3, RSRV_STR4, RSRV_STR5, to_char(RSRV_DATE1,'yyyy-mm-dd hh24:mi:ss') RSRV_DATE1, to_char(RSRV_DATE2,'yyyy-mm-dd hh24:mi:ss') RSRV_DATE2, to_char(RSRV_DATE3,'yyyy-mm-dd hh24:mi:ss') RSRV_DATE3, RSRV_TAG1, RSRV_TAG2, RSRV_TAG3, TO_CHAR(INST_ID) INST_ID
from TF_F_USER_GRP_PACKAGE
where 
 USER_ID=to_number(:USER_ID)
 and PARTITION_ID=MOD(to_number(:USER_ID),10000)
 and PRODUCT_ID=:PRODUCT_ID
 AND PACKAGE_ID=:PACKAGE_ID
 AND ELEMENT_TYPE_CODE=:ELEMENT_TYPE_CODE
 AND ELEMENT_ID=:ELEMENT_ID
 AND SYSDATE < END_DATE