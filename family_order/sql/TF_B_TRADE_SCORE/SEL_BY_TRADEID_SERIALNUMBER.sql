SELECT A.TRADE_ID,
       A.ACCEPT_MONTH,
       A.USER_ID,
       A.SERIAL_NUMBER,
       A.ID_TYPE,
       A.SCORE_TYPE_CODE,
       A.YEAR_ID,
       A.START_CYCLE_ID,
       A.END_CYCLE_ID,
       A.SCORE,
       A.SCORE_CHANGED,
       TO_CHAR(NVL(A.VALUE_CHANGED / 100, 0)) VALUE_CHANGED,
       A.SCORE_TAG,
       A.RULE_ID,
       A.ACTION_COUNT,
       A.RES_ID,
       A.GOODS_NAME,
       A.CANCEL_TAG,
       A.UPDATE_TIME,
       A.UPDATE_STAFF_ID,
       A.UPDATE_DEPART_ID,
       A.REMARK,
       A.RSRV_STR1,
       A.RSRV_STR2,
       A.RSRV_STR3,
       A.RSRV_STR4,
       A.RSRV_STR5,
       A.RSRV_STR6,
       A.RSRV_STR7,
       A.RSRV_STR8,
       A.RSRV_STR9,
       A.RSRV_STR10
  FROM TF_B_TRADE_SCORE A
  WHERE A.TRADE_ID = :TRADE_ID
   AND A.ACCEPT_MONTH = TO_NUMBER(SUBSTR(:TRADE_ID, 5, 2))
   AND A.SERIAL_NUMBER = :SERIAL_NUMBER