       T.DATA0,
       T.DATA1,
       T.DATA2,
       T.DATA3,
       T.DATA4,
       T.DATA5,
       T.DATA6,
       T.DATA7,
       T.DATA8,
       T.DATA9,
       T.REMARK,
       T.VALID_TAG,
       T.ACCEPT_DEPART_ID,
       T.UPDATE_DEPART_ID,
       T.UPDATE_STAFF_ID,
       T.ACCEPT_STAFF_ID,
       TO_CHAR(T.CREATE_DATE, 'YYYY-MM-DD HH24:MI:SS') CREATE_DATE,
       TO_CHAR(T.UPDATE_DATE, 'YYYY-MM-DD HH24:MI:SS') UPDATE_DATE
  FROM TD_B_EWE_TEMPLET_STATIC T
 WHERE T.BPM_TEMPLET_ID = :BPM_TEMPLET_ID
   AND T.VALID_TAG = :VALID_TAG
