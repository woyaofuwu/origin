UPDATE TF_B_TRADE_ATTR
   set ATTR_VALUE = :ATTR_VALUE,
       RSRV_TAG1  = :RSRV_TAG1,
       RSRV_STR2  = :RSRV_STR2,
       RSRV_STR4  = :RSRV_STR4,
       RSRV_STR5  = :RSRV_STR5
 where TRADE_ID = TO_NUMBER(:TRADE_ID)
   AND ACCEPT_MONTH = TO_NUMBER(SUBSTR(:TRADE_ID, 5, 2))
   AND INST_TYPE = :INST_TYPE
   AND ATTR_CODE = :ATTR_CODE
   AND ATTR_VALUE = :OLD_ATTR_VALUE
   and USER_ID = :USER_ID