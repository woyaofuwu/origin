SELECT 
   T.ARCHIVES_ID ,
  T.AGREEMENT_ID ,
  T.A_NAME,
  T.A_ADDRESS,
  T.A_HEADER,
  T.A_CONTACT_PHONE,
  T.A_BANK,
  T.A_BANK_ACCT_NO,
  T.A_SIGN_MAN,
  TO_CHAR(T.A_SIGN_DATE,'YYYYMMDDHH24MISS') A_SIGN_DATE,
  T.B_NAME ,
  T.B_ADDRESS,
  T.B_HEADER,
  T.B_CONTACT_PHONE,
  T.B_BANK,
  T.B_BANK_ACCT_NO ,
   TO_CHAR(T.B_SIGN_DATE,'YYYYMMDDHH24MISS') B_SIGN_DATE,
  T.B_SIGN_MAN,
  T.PDF_FILE ,
  T.PRODUCT_ID,
  T.CONTRACT_CODE,
  TO_CHAR(C.END_DATE ,'YYYYMMDDHH24MISS') START_DATE,
  TO_CHAR(C.END_DATE ,'YYYYMMDDHH24MISS') END_DATE,
  C.ARCHIVES_NAME
   FROM TF_F_ELECTRONIC_AGREEMENT T,TF_F_ELECTRONIC_ARCHIVES C
  WHERE C.ARCHIVES_ID = T.ARCHIVES_ID AND T.AGREEMENT_ID = :AGREEMENT_ID