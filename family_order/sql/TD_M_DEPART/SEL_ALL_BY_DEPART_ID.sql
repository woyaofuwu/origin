SELECT DEPART_ID,DEPART_CODE,DEPART_NAME,DEPART_KIND_CODE,DEPART_FRAME,VALIDFLAG,AREA_CODE,PARENT_DEPART_ID,MANAGER_ID,ORDER_NO,USER_DEPART_CODE,to_char(START_DATE,'yyyy-mm-dd hh24:mi:ss') START_DATE,to_char(END_DATE,'yyyy-mm-dd hh24:mi:ss') END_DATE,DEPART_LEVEL,REMARK,RSVALUE1,RSVALUE2,RSVALUE3,RSVALUE4
  FROM TD_M_DEPART
 WHERE depart_frame LIKE (
       SELECT depart_frame FROM td_m_depart WHERE depart_id=:DEPART_ID)||'%'
   AND validflag='0'
   ORDER BY depart_level