SELECT IMSI
   FROM TF_F_USER_RES A
  WHERE RES_TYPE_CODE = '1'
    AND SYSDATE < END_DATE
    AND EXISTS
  (SELECT 1
           FROM TF_F_USER B, TF_F_RELATION_UU D
          WHERE A.USER_ID = B.USER_ID
            AND A.USER_ID = D.USER_ID_B
            AND A.PARTITION_ID = B.PARTITION_ID
            AND A.PARTITION_ID = D.PARTITION_ID
            AND B.REMOVE_TAG = '0'
            AND D.RELATION_TYPE_CODE = '20'
            AND SYSDATE < D.END_DATE)