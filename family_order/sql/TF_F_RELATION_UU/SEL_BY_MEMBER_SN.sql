SELECT G.CUST_NAME,A.*
  FROM TF_F_RELATION_UU A,TF_F_USER_PRODUCT P,TF_F_USER_SVC V,TF_F_CUST_GROUP G,TF_F_USER U
 WHERE 1=1
 AND A.RELATION_TYPE_CODE = :RELATION_TYPE_CODE
 AND A.ROLE_CODE_B = '1'
 AND A.ROLE_CODE_A = '0'
 AND A.SERIAL_NUMBER_B = :SERIAL_NUMBER
 AND A.USER_ID_B = :USER_ID
 AND A.USER_ID_A = P.USER_ID
 AND (P.PRODUCT_ID = 20005015 OR P.PRODUCT_ID = 20005013)
 AND (V.SERVICE_ID = 99010013 OR V.SERVICE_ID = 99010012)
 AND P.END_DATE > SYSDATE
 AND V.END_DATE > SYSDATE
 AND U.SERIAL_NUMBER = A.SERIAL_NUMBER_A
 AND U.REMOVE_TAG = '0'
 AND U.CUST_ID = G.CUST_ID
 AND G.REMOVE_TAG = '0'
 