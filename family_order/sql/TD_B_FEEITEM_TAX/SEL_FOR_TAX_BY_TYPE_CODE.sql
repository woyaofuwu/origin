SELECT TYPE, RATE, FEE, FEE_TYPE_CODE,RES_TAG,RSRV_NUM1
  FROM TD_B_ELEMENT_TAX T
 WHERE T.TRADE_TYPE_CODE = :TRADE_TYPE_CODE
   AND T.FEE_TYPE_CODE = :FEE_TYPE_CODE
   AND T.FEE_MODE = :FEE_MODE
   AND T.PRODUCT_ID = :PRODUCT_ID 
   AND T.PACKAGE_ID =:PACKAGE_ID
   AND T.ELEMENT_ID =:ELEMENT_ID
   AND TO_DATE(:NOW_DATE, 'YYYY-MM-DD hh24:mi:ss') BETWEEN T.START_DATE AND
       T.END_DATE
   AND T.EPARCHY_CODE = :EPARCHY_CODE
UNION
SELECT TYPE, RATE, FEE, FEE_TYPE_CODE,RES_TAG,RSRV_NUM1
  FROM TD_B_FEEITEM_TAX S
 WHERE S.TRADE_TYPE_CODE = :TRADE_TYPE_CODE
   AND S.FEE_TYPE_CODE = :FEE_TYPE_CODE
   AND S.FEE_MODE = :FEE_MODE
   AND S.PRODUCT_ID = :PRODUCT_ID 
   AND TO_DATE(:NOW_DATE, 'YYYY-MM-DD hh24:mi:ss') BETWEEN S.START_DATE AND
       S.END_DATE
   AND S.EPARCHY_CODE = :EPARCHY_CODE