--IS_CACHE=Y
SELECT NODE_CODE,NODE_NAME,NODE_TYPE,PARENT_NODE_CODE,NODE_LEVEL,(B.NODE_COUNT+C.NODE_COUNT)NODE_COUNT
FROM TD_S_CONTENT_CATEGORY A,
  (SELECT
    COUNT(*)NODE_COUNT
   FROM
      TD_S_CONTENT_CATEGORY
   WHERE
     PARENT_NODE_CODE IN
      (SELECT
         NODE_CODE
    FROM
      TD_S_CONTENT_CATEGORY
       WHERE
       PARENT_NODE_CODE=:PARENT_NODE_CODE)
     ) B,
  (SELECT
    COUNT(*)NODE_COUNT
  FROM
    TD_S_CONT_CATEGORY_RELATION
  WHERE
    NODE_CODE in
      (SELECT
         NODE_CODE
        FROM
        TD_S_CONTENT_CATEGORY
       WHERE
       PARENT_NODE_CODE=:PARENT_NODE_CODE)
  )C
WHERE A.PARENT_NODE_CODE=:PARENT_NODE_CODE