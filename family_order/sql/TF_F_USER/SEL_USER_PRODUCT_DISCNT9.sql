SELECT U.SERIAL_NUMBER 
  FROM TF_F_USER PARTITION(PAR_TF_F_USER_9) U,
       TF_F_USER_PRODUCT PARTITION(PAR_TF_F_USER_PRODUCT_9) P
 WHERE U.USER_ID = P.USER_ID 
   AND U.USER_STATE_CODESET = '0'
   AND U.REMOVE_TAG = '0'
   AND P.PRODUCT_ID = '20171215' 
   AND SYSDATE BETWEEN P.START_DATE AND P.END_DATE 
   AND NOT EXISTS(
            SELECT 1 
              FROM TF_F_USER_DISCNT PARTITION(PAR_TF_F_USER_DISCNT_9) T 
             WHERE T.USER_ID = U.USER_ID
               AND T.DISCNT_CODE IN ('1215001','1215002','1215003','1218101','1218102','1218103','1218201','1218202','1218203')  
               AND SYSDATE BETWEEN T.START_DATE AND T.END_DATE )
