SELECT A.SERIAL_NUMBER,
       TO_CHAR(A.ACCEPT_DATE, 'yyyy-mm-dd hh24:mi:ss') PARA_CODE2,
       DECODE(A.MAKE_STATE, '0', '定制状态', 'D', '定制取消', '其他状态') PARA_CODE3,
       DECODE(A.MAKE_TERMINAL, '11000040', '语音交易', '终端交易') PARA_CODE4,
       A.COMMENDER_ID PARA_CODE5,
       A.UPDATE_TIME PARA_CODE6,
       A.UPDATE_STAFF_ID PARA_CODE7,
       A.UPDATE_DEPART_ID PARA_CODE8,
       TO_CHAR(A.START_DATE, 'yyyy-mm-dd hh24:mi:ss') PARA_CODE16,
       TO_CHAR(A.END_DATE, 'yyyy-mm-dd hh24:mi:ss') PARA_CODE17,
       A.CANCEL_STAFF_ID PARA_CODE18,
       A.CANCEL_DEPART_ID PARA_CODE19,
       A.REMARK REMARK
FROM TF_A_BANKPACKET A, TF_F_USER B
WHERE (B.SERIAL_NUMBER >= :START_SERIALNUMBER OR :START_SERIALNUMBER IS NULL)
   AND (B.SERIAL_NUMBER <= :END_SERIALNUMBER OR :END_SERIALNUMBER IS NULL)
   AND B.REMOVE_TAG = '0'
   AND A.USER_ID = B.USER_ID
   AND SYSDATE BETWEEN A.START_DATE AND A.END_DATE
   AND A.ACCEPT_DATE BETWEEN TO_DATE(:START_DATE, 'yyyy-mm-dd') AND
       TO_DATE(:END_DATE, 'yyyy-mm-dd')
   AND (A.MAKE_STATE = :MAKE_STATE OR :MAKE_STATE IS NULL)