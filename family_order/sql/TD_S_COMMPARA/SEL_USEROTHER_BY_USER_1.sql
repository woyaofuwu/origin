SELECT A.RSRV_NUM1 FOREGIFT_CODE,
       A.RSRV_NUM2 MONEY,
       A.SERIAL_NUMBER INVOICE_NO,
       A.PROCESS_TAG,
       TO_CHAR(A.START_DATE, 'YYYY-MM-DD HH24:MI:SS') START_DATE,
       A.RSRV_STR8 COLLECT_STAFF_ID,
       A.RSRV_STR1 COLLECT_TRADE_ID,
       TO_CHAR(A.END_DATE, 'YYYY-MM-DD HH24:MI:SS') END_DATE,
       A.STAFF_ID CLEAR_STAFF_ID,
       A.RSRV_STR3 CLEAR_TRADE_ID,
       A.RSRV_STR2 CUST_NAME
  FROM TF_F_USER_OTHERSERV A, TF_F_USER B
 WHERE A.PARTITION_ID = MOD(TO_NUMBER(A.USER_ID), 10000) 
	 AND A.USER_ID = B.USER_ID  
   AND B.SERIAL_NUMBER = :PARA_CODE1
   AND A.SERVICE_MODE = 'FG'
   AND B.REMOVE_TAG not in ( '0','5')  --销户的状况