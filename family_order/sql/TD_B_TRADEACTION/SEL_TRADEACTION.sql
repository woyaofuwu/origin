SELECT *
FROM TD_B_TRADEACTION
        WHERE STATE = '1'
        AND (TRADE_TYPE_CODE = :TRADE_TYPE_CODE OR TRADE_TYPE_CODE = '-1')
        AND (IN_MODE_CODE = :IN_MODE_CODE OR IN_MODE_CODE = '-1')
        AND (ORDER_TYPE_CODE = :ORDER_TYPE_CODE OR ORDER_TYPE_CODE = '-1')
        AND Act_Type = 'reg'
        ORDER BY ORDER_NO ASC
